	<h2>Our Prague highlights</h2>
	<p>
		We've compiled a list and <a href="https://mapsengine.google.com/map/edit?mid=zPDpRcNOoekY.keNH3o1L7NOc" target="_blank">map</a> of some of the most famous tourist attractions in Prague.
		The list is roughly follows a route that starts up on the hill near the Castle, continues down into the Old Town, via the Charles Bridge, and onto Wenceslas Square.
		Sights are marked with red pins and places to eat and drink are marked in orange. 
	</p>

	<h3><a href="http://en.wikipedia.org/wiki/Pet%C5%99%C3%ADn_Lookout_Tower">Petřín Tower and Funicular Railway</a></h3>
	<p>
		Prague’s own mini version of the Eiffel Tower has some of the best views over the city. 
		If you have children (or don’t) you might enjoy the hall of mirrors at the base of the tower. 
		The best way to get to Petřín tower is to take the funicular railway, which starts near the Újezd tram stop. 
		You can use your bus/tram/metro ticket on the funicular railway.
	</p>

	<h3><a href="http://en.wikipedia.org/wiki/Prague_Castle">Prague Castle (Pražský Hrad)</a></h3>
	<p>
		Prague Castle is the largest ancient castle in the world and has overlooked the city since 870. 
		In July, the castle is open every day from 05:00 until 24:00 and you can see the changing of the guard by the castle gates on the hour from 07:00 to 20:00. 
		St. Vitus Cathedral is also part of the castle complex.
		Entry to the castle complex is free. 
		However, you must buy tickets in order to climb some of the towers and enter certain exhibitions.
		Long and short guided tours are also available (<a href="http://www.hrad.cz/en/prague-castle/prague-castle-tourist-information/tickets.shtml">see details</a>).
	</p>

	<h3><a href="http://en.wikipedia.org/wiki/Charles_Bridge">The Charles Bridge (Karlův Most)</a></h3>
	<p>
		The Charles Bridge takes you from the Castle to the Old Town. 
		75 baroque statues and countless street performers and souvenir stalls line the pedestrian bridge. 
		There is a gothic gateway at either end of the bridge, which you can climb. 
		The bridge has great views looking up to the castle and along the River Vltava. 
	</p>

	<h3><a href="http://en.wikipedia.org/wiki/Old_Town_Square_(Prague)">Old Town Square (Staroměstské Náměstí)</a></h3>
	<p>
		The Old Town Square houses Prague Town Hall, the Tyn Church, St Nicholas Church and, at its centre, the Jan Hus Monument. 
		The most famous attraction of the square is the Astronomical Clock, with its mechanical figures. 
		You can see the clock in action on the hour from 08:00 to 20:00. 
		There are lots of interesting shops in the winding streets that surround the square.
	</p>

	<h3><a href="http://en.wikipedia.org/wiki/Wenceslas_Square">Wenceslas Square (Václavské Náměstí)</a></h3>
	<p>
		Wenceslas Square is Prague’s other main square. 
		The square is 750 metres long and is overlooked by the Wenceslas Monument and the Czech National Museum (currently closed for renovations). 
		The square is lined with shops and attractions.
		Although modest and above a McDonalds, we found that the Museum of Communism at the bottom end of the square was particularly interesting. 
		The square is also the main destination for British stag parties; so some of the pubs are a bit seedy (and the beer is a lot more expensive!).
	</p>

	<h3><a href="http://en.wikipedia.org/wiki/Dancing_House">The Dancing House (Tančící dům)</a></h3>
	<p>
		The Dancing House is the nickname for the distinctive Nationale-Nederlanden building, which was designed by Vlado Milunić and Frank Gehry. 
		The Dancing House is a short walk from the yellow line metro station Karlovo Náměstí. 
		You can take a yellow line train from Můstek if you are at Wenceslas Square.
	</p>

